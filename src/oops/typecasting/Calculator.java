package oops.typecasting;

public class Calculator {

	public int addition(int a, int b) {
		return a + b;
	}

	final protected int subtraction(int a, int b) {
		return a - b;
	}

	private int multiplication(int a, int b) {
		return a * b;
	}

	static int division(int a, int b) {
		return a / b;
	}	
}
